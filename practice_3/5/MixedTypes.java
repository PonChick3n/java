public class MixedTypes {
    public static void main(String[] args) {
        byte b = 10;
        short s = 20;
        int i = 30;
        long l = 40L;
        float f = 5.5f;
        double d = 6.6;

        System.out.println("byte + short + int + long + float + double: " + (b + s + i + l + f + d));
    }
}